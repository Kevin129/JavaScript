//Pętle

//FOR
for(let i = 0; i<10; i++) {
    console.log("Testowanie pętli FOR - " + i)
}

//WHILE

let licznik = 0;

while(licznik < 10) {
    console.log("Testowanie pętli while")
    licznik ++;
}

//DO WHILE

let licznik = 0;

do {
    console.log("Testowanie pętli DO WHILE")
    licznik++
} while(licznik < 10)